package Chapter1;

public class MinMax {
    public static void main(String[] args) {
        //single line comment - Get started with min and max values
        System.out.println("Min Integer Value: " + Integer.MIN_VALUE);
        System.out.println("Max Integer Value: " + Integer.MAX_VALUE);

        /*Multiple line comment
        * Get started with min and max bits
        * */
        System.out.println("Min Bits for Float: " + Float.MIN_VALUE);
        System.out.println("Min Bits for Byte: " + Byte.MIN_VALUE);
    }
}
